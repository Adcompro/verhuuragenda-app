class _AppLocalizationsDelegate extends LocalizationsDelegate<AppLocalizations> {
  const _AppLocalizationsDelegate();
  @override Future<AppLocalizations> load(Locale locale) { return SynchronousFuture<AppLocalizations>(lookupAppLocalizations(locale)); }
  @override bool isSupported(Locale locale) => <String>['de', 'en', 'es', 'fr', 'it', 'nl'].contains(locale.languageCode);
  @override bool shouldReload(_AppLocalizationsDelegate old) => false;
}
AppLocalizations lookupAppLocalizations(Locale locale) {
  switch (locale.languageCode) { case 'de': return AppLocalizationsDe(); case 'en': return AppLocalizationsEn(); case 'es': return AppLocalizationsEs(); case 'fr': return AppLocalizationsFr(); case 'it': return AppLocalizationsIt(); case 'nl': return AppLocalizationsNl(); }
  return AppLocalizationsNl();
}
